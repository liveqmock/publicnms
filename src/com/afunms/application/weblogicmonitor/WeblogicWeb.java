package com.afunms.application.weblogicmonitor;

public class WeblogicWeb {
	String webAppComponentRuntimeComponentName = null; //	Component名
	String webAppComponentRuntimeStatus = null; //	状态
	String webAppComponentRuntimeOpenSessionsCurrentCount = null; //	当前会话数
	String webAppComponentRuntimeOpenSessionsHighCount = null; //	最大会话数
	String webAppComponentRuntimeSessionsOpenedTotalCount = null; //	总会话数
	public String getWebAppComponentRuntimeComponentName() {
		return webAppComponentRuntimeComponentName;
	}
	public void setWebAppComponentRuntimeComponentName(
			String webAppComponentRuntimeComponentName) {
		this.webAppComponentRuntimeComponentName = webAppComponentRuntimeComponentName;
	}
	public String getWebAppComponentRuntimeStatus() {
		return webAppComponentRuntimeStatus;
	}
	public void setWebAppComponentRuntimeStatus(String webAppComponentRuntimeStatus) {
		this.webAppComponentRuntimeStatus = webAppComponentRuntimeStatus;
	}
	public String getWebAppComponentRuntimeOpenSessionsCurrentCount() {
		return webAppComponentRuntimeOpenSessionsCurrentCount;
	}
	public void setWebAppComponentRuntimeOpenSessionsCurrentCount(
			String webAppComponentRuntimeOpenSessionsCurrentCount) {
		this.webAppComponentRuntimeOpenSessionsCurrentCount = webAppComponentRuntimeOpenSessionsCurrentCount;
	}
	public String getWebAppComponentRuntimeOpenSessionsHighCount() {
		return webAppComponentRuntimeOpenSessionsHighCount;
	}
	public void setWebAppComponentRuntimeOpenSessionsHighCount(
			String webAppComponentRuntimeOpenSessionsHighCount) {
		this.webAppComponentRuntimeOpenSessionsHighCount = webAppComponentRuntimeOpenSessionsHighCount;
	}
	public String getWebAppComponentRuntimeSessionsOpenedTotalCount() {
		return webAppComponentRuntimeSessionsOpenedTotalCount;
	}
	public void setWebAppComponentRuntimeSessionsOpenedTotalCount(
			String webAppComponentRuntimeSessionsOpenedTotalCount) {
		this.webAppComponentRuntimeSessionsOpenedTotalCount = webAppComponentRuntimeSessionsOpenedTotalCount;
	}
	
	

}
